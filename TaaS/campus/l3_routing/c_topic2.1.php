    <p class="fs-6"><h5 class="text-warning">Introduction to Network Routing</h5>
    <b class="text-warning">Network routing</b> is the process of selecting a path across one or more networks. In packet-switching networks, These routing decisions are made by specialized pieces of network hardware called routers and multi-layer switches. <br><br>
    <i class="text-warning">The following are the Network Routing Protocols used:</i> <br>
    <b class="text-info">BGP</b>: The Border Gateway Protocol (BGP) routing protocol is the main routing protocol of the internet. <br>
    <b class="text-info">OSPF</b>: The Open Shortest Path First (OSPF) protocol is commonly used by network routers to dynamically identify the fastest and shortest available routes for sending packets to their destination. <br>
    <b class="text-info">RIP</b>: The Routing Information Protocol (RIP) uses "hop count" to find the shortest path from one network to another by "hop counts". <br>
    <b class="text-info">EIGRP and ISIS</b>: EIGRP (the Enhanced Interior Gateway Routing Protocol) and IS-IS (Intermediate System to Intermediate System). Both are mainly for use with Cisco Devices. <br>
    <b class="text-info">Static Routing</b>: This routing protocol is designed to force a mannual entry of a destination subnet. <br> <br>
