    <p class="fs-6"><h5 class="text-warning">IP Fabric Considerations</h5>
    The following are the considerations when configuring a Spine layer in a IP Fabric Architecture. Some of the following concepts and terminologies will be re-used as the task move forward. <br><br>
    <li><b class="text-warning">AS or AS Number</b>: It's a unique identifier assigned to an Autonomous System (AS) on the internet. An AS is essentially a group of interconnected networks under a single administrative control that share a common routing policy. </li>
    <li><b class="text-warning">eBGP (External Border Gateway Protocol)</b>: Operates between Autonomous Systems (AS) - eBGP is used by border routers at the edge of an AS to communicate and exchange routing information with routers in other, neighboring ASes. This allows different networks to connect and share reachability information for various destinations on the internet.</li>
    <li><b class="text-warning">iBGP (Internal Border Gateway Protocol)</b>: Operates within an Autonomous System (AS) -  iBGP is used by routers inside a single AS to share routing information with each other. It ensures all routers within the AS have a consistent view of the network topology and can efficiently route traffic to different destinations.</li>
    <li><b class="text-warning">L3 Links, short for Layer 3 Links</b>: fer to connections that operate at the network layer (Layer 3) of the Open Systems Interconnection (OSI) model. This layer is responsible for logical addressing and routing data packets across networks.</li><br>
